# Problem 2: A while loop that creates a list with number 0 to 10

# Initialize an empty list
L = []

# Initialize counter variable
counter = 0

# Use a while loop to append numbers 0 to 10
while counter <= 10:
    L.append(counter)
    counter += 1

# Print the final list
print(L)